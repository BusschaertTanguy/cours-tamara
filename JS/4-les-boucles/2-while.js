// 1. Créer une boucle qui à chaque itération incrémente une variable de 2 (la valeur de début de la variable est 0).
// À chaque itération, on doit loger le nouveau résultat de la variable.
// Nous voulons incrémenter la variable jusqu'à ce qu'elle atteigne 1000 ou plus.

let resultat = 0;

while(resultat < 1000) {
    resultat = resultat + 2;
    console.log(resultat);
}